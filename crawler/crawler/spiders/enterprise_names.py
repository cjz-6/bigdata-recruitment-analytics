# 生成搜索用的关键词列表（取公司简称用于 51job 搜索）
def get_search_names():
    """返回适合在招聘网站搜索的公司名（取核心名称）"""
    names = []
    for tag, companies in ENTERPRISE_LIST.items():
        for c in companies:
            # 取公司名核心部分（前 8-12 字）用于搜索
            short = c.replace('有限公司', '').replace('股份', '').replace('有限责任', '')
            short = short.replace('（上海）', '').replace('（深圳）', '').replace('（北京）', '')
            short = short.strip()
            if len(short) > 12:
                short = short[:12]
            if short and short not in names:
                names.append(short)
    return names

def match_enterprise(company_name):
    """检查公司名是否匹配已知企业名单，返回企业类型标签"""
    if not company_name:
        return None
    for tag, companies in ENTERPRISE_LIST.items():
        for c in companies:
            # 模糊匹配：去掉"有限公司"等后缀后比对
            if company_name in c or c in company_name:
                return tag
            # 取核心名匹配
            core = c.replace('有限公司', '').replace('股份', '').replace('有限责任', '').strip()
            if core and len(core) >= 4 and core[:6] in company_name:
                return tag
    return None
